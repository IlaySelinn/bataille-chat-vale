package school.coda.ilay_luisa.bataillejavale.model;

public class CoordinatePoints {
    private final int row;
    private final int col;

    public CoordinatePoints(int row, int col) {
        this.row = row;
        this.col = col;
    }

    public int getRow() {
        return row;
    }
    public int getCol() {
        return col;
    }
}
